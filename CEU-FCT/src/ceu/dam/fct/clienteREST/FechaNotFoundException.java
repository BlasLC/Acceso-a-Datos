package ceu.dam.fct.clienteREST;

public class FechaNotFoundException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 8343153111768348921L;

	public FechaNotFoundException() {
	}

	public FechaNotFoundException(String message) {
		super(message);

	}

	public FechaNotFoundException(Throwable cause) {
		super(cause);

	}

	public FechaNotFoundException(String message, Throwable cause) {
		super(message, cause);

	}

	public FechaNotFoundException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

}
