package xml;

public class CursosXMLException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 8081004349854693547L;

	public CursosXMLException() {
	}

	public CursosXMLException(String message) {
		super(message);

	}

	public CursosXMLException(Throwable cause) {
		super(cause);

	}

	public CursosXMLException(String message, Throwable cause) {
		super(message, cause);

	}

	public CursosXMLException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

}
